#ifndef WATCHDOG_H
#define WATCHDOG_H

#include <stdint.h>

/* ══════════════════════════════════════════════════════════════════════════
   Сторожевой таймер (FWDGT — Free Watchdog Timer).
   ══════════════════════════════════════════════════════════════════════════
   Аппаратный «зависатель» на IRC40K: если ПО не сбрасывает счётчик в
   течение wdt_init() мс — контроллер автоматически перезагружается.
   После сброса по WDT в RCU_RSTSCK взводится флаг FWDGTRSTF — функция
   wdt_was_reset_cause() возвращает 1 на первом запуске после такого
   события и атомарно очищает флаг.                                       */

/* Инициализация сторожа на timeout_ms миллисекунд (ориентировочно;
   IRC40K имеет разброс ±10 %).  Запускается сразу после вызова — больше
   отключить FWDGT нельзя до следующего сброса.                            */
void wdt_init(uint32_t timeout_ms);

/* Сбросить (kick) счётчик сторожа.  Вызывать в каждом «живом» цикле задач
   с запасом не менее 2× относительно timeout_ms.                          */
void wdt_kick(void);

/* 1 — последний сброс был вызван FWDGT, 0 — иначе. Вызывать ОДИН раз
   на старте (до wdt_init).  Внутри сама очищает флаги в RCU_RSTSCK.       */
uint8_t wdt_was_reset_cause(void);

#endif /* WATCHDOG_H */
